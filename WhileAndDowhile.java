import java.util.*;
public class WhileAndDowhile {
    public static void main(String args[]){
        int x=10;
        //While loop is ENTRY CONTOLLED
        //here loop executes only if contition satisfies
        while(x<9){
            System.out.println(" HOLA  ! ");
        }
        //do while loop is EXIT CONTROLLED
        //here loop executes for 1 time for sure
        do{
            System.out.println(" Hii !!");
        }while(x<9);
    }
}
